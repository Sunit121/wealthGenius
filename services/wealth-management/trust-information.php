                <p data-aos="fade-up">A settlor is a person who has put property into trust. Property is normally put into the trust when it is created, but it can also be added at a later date.</p>
                <h4 class="mt-5" data-aos="fade-up">Is a settlement the same as a trust?</h4>     
                <p data-aos="fade-up">The words 'settlement' and 'trust' are sometimes used in place of each other, and to describe the same thing. For tax purposes, the term 'settlement' can have a wider meaning and can include various other arrangements and agreements.</p>
