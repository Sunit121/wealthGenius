                <h3 data-aos="fade-up">What is the Enterprise Investment Scheme?</h3>
                <p data-aos="fade-up">The Enterprise Investment Scheme (EIS) is a UK Government investment programme designed to help small companies that are considered risky to attract investors. Investors that buy shares in these risky - but often innovative - companies will receive tax relief, making an investment more lucrative.</p>
