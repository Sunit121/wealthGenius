                <p data-aos="fade-up">It should be noted that the information contained in this section relates to our current understanding of the law of England & Wales as it relates to Lasting Power of Attorney which is subject to change. Laws in other parts of the UK will differ.</p>
                <h3 data-aos="fade-up">Managing your affairs and lasting power of attorney</h3>
                <p data-aos="fade-up">There may come a time when, because you are incapable of managing your property and financial affairs or personal welfare, you will need someone to do this for you.</p>
                <p data-aos="fade-up">You can formally appoint a friend, relative or professional to hold a lasting power of attorney that will allow them to act on your behalf. Lasting power of attorney (LPA) in England and Wales has no legal standing until it is registered with the Office of the Public Guardian. A lasting power of attorney is a legal document that lets you appoint someone you trust as an 'attorney' to make decisions on your behalf.</p>
                <p data-aos="fade-up">It can be drawn up at any time while you have capacity but has no legal standing until it is registered with the Office of the Public Guardian.</p>
                <p data-aos="fade-up">A registered LPA can, unless the document states differently, be used at any time, whether you have the mental ability to act for yourself or not.</p>
                <p data-aos="fade-up">You can create two types of LPA:</p>
                <ul>
                    <li>Property and financial affairs</li>
                    <li>Health and welfare</li>
                </ul>
                <h4 class="mt-5" data-aos="fade-up">Property and financial affairs</h4>     
                <p data-aos="fade-up">A Property and financial affairs allows you to choose someone to make decisions about how to spend your money and the way your property and affairs are managed.</p>
                <h4 class="mt-5" data-aos="fade-up">Health and welfare</h4>     
                <p data-aos="fade-up">A Health and welfare allows you to choose someone to make decisions about your healthcare and welfare. This includes decisions to refuse or consent to treatment on your behalf and deciding where you live. These decisions can only be taken on your behalf when the LPA is registered and you lack the capacity to make the decisions yourself.</p>
                <h4 class="mt-5" data-aos="fade-up">How many people should you appoint and whom?</h4>     
                <p data-aos="fade-up">You may not be able to check up on the attorney yourself if you become incapable, so it may be a good idea to appoint more than one person to help prevent abuse of the responsibility. Choose people you can trust to act in your best interests. You should consider how well they look after their own financial affairs and whether you can trust them to use your money to meet your needs.</p>
                <p data-aos="fade-up">Depending on the complexity of your property and financial affairs it may be a good idea to get advice from a solicitor before making an LPA.</p>
                <p data-aos="fade-up">You can get further advice from the Office of the Public Guardian about making an LPA.</p>

                <h4 class="mt-5" data-aos="fade-up">Registering an LPA</h4>     
                <p data-aos="fade-up">Either you or your attorney can apply to the Public Guardian to register your LPA. The application can be made at any time after you have made an LPA.</p>
                <p data-aos="fade-up">Before the application to register the LPA is made, the people named as being entitled to receive notification of the application must be told by the person who wants to register it.</p>
                <p data-aos="fade-up">The Public Guardian will give notice that the application has been received to:</p>
                <ul>
                    <li>You as the donor</li>
                    <li>The attorney or attorneys</li>
                </ul>
                <p data-aos="fade-up">Your relatives will not be notified of the application to register the LPA unless you have named them as persons who should be given notice. Anyone who has been notified can object to the LPA being registered.</p>                    
                <p data-aos="fade-up">Once the LPA is registered it continues indefinitely. The LPA can be registered by the attorney after you have lost capacity.</p>
                <p data-aos="fade-up">The lasting power of attorney replaced the enduring power of attorney (EPA) on 1 October 2007. A person given power under an EPA before 1 October 2007 can still use it and apply to have it registered. This person has a duty to apply to register with the EPA as soon as they believe that you are becoming or have become mentally incapable of making financial decisions for yourself.</p>
                <p data-aos="fade-up">If you have an unregistered EPA and still have the capacity to make decisions for yourself, you can make a Health and welfare to run alongside it.</p>
                
                <h4 class="mt-5" data-aos="fade-up">Cancelling powers of attorney</h4>
                <p data-aos="fade-up"><b>Lasting power of attorney (LPA)</b></p>   

                <p data-aos="fade-up">You can cancel your LPA if you have the mental capacity to do so. If there is a dispute about whether your LPA has been cancelled, the Court of Protection has the authority to make a decision.</p>
                <p data-aos="fade-up">A Property and financial affairs is revoked if you or your attorney becomes bankrupt; bankruptcy does not terminate a Health and welfare.</p>
                <p data-aos="fade-up"><b>Enduring power of attorney (EPA)</b></p>   

                <p data-aos="fade-up">You can cancel an unregistered EPA if you have the mental capacity to do so, without applying to the Court of Protection.</p>
                <p data-aos="fade-up">To cancel a registered EPA you must show the Court of Protection:</p>
                <ul>
                    <li>that you understand who the attorney is and what powers they have</li>
                    <li>that you understand the effect of the cancellation</li>
                    <li>why the EPA needs to be cancelled</li>
                </ul>
                <p data-aos="fade-up">An EPA is revoked if you or the appointed attorney becomes bankrupt.</p>
