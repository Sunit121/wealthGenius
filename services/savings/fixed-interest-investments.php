                <p data-aos="fade-up">The risk of fixed-interest investments is that the bond issuer defaults on either the interest payments or the repayment of capital.  Historically speaking fixed interest investments have not provided the same levels of return as equity investments, but the risk to an investor’s capital is generally lower.</p>
