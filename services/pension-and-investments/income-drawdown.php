                <h3>How Income Drawdown Works</h3>    
                <p>Rather than exchanging your pension savings for an annuity (a fixed and regular income for life paid by the pension provider) the pension fund is left invested and you draw income directly from the fund. As the bulk of your pension remains invested the fund is still able to benefit from any growth (or not!) in the value of its investments. There’s no limit to the amount of income you can withdraw — you can draw as much (or as little) as you like, even the entire fund if you want.</p>
                    <p>And unlike an annuity, in a drawdown arrangement, the pension saver retains access to their pension pot.</p>
