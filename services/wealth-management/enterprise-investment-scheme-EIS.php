                    <li>Have a maximum of £15 million in gross assets</li>
                    <li>Have a maximum of 250 employees</li>
                    <li>Have at least seven years since the business’s first commercial sale</li>
                    <li>Not already be trading shares on a recognised stock exchange</li>
                </ul>
