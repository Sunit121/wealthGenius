                <li>if you make a loss you may be able to make a claim for that loss and deduct it from other gains, but only if the asset normally attracts CGT</li>
                <li>if someone dies and leaves their belongings to their beneficiaries, there is no CGT to pay at that time - however if an asset is later disposed of by a beneficiary, any CGT they may have to pay will be based on the difference between the market value at the time of death and the value at the time of disposal</li>
