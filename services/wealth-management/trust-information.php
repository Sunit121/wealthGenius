                    <li>Land and buildings</li>
                    <li>Other assets, such as paintings</li>
                </ul>
                <p data-aos="fade-up">The cash and investments held in the trust are also called the 'capital' or 'fund' of the trust. This capital (or fund) may produce income, such as interest or dividends. The land and buildings may produce rental income.</p>
                <h4 class="mt-5" data-aos="fade-up">What is a 'beneficiary'?</h4>     
                <p data-aos="fade-up">A beneficiary is anyone who benefits from the property held in the trust. There can be one or more beneficiaries, such as a whole family or a class of people, and each may benefit from the trust in a different way. </p>
                <p data-aos="fade-up">For example, a beneficiary may benefit from</p>
                <ul>
                    <li>the income only, or</li>
                    <li>the capital only, or</li>
