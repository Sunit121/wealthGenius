                <h3 data-aos="fade-up">Introduction to Trusts</h3>
                <p data-aos="fade-up">A trust is an obligation binding in a person (which can be an individual or a company) called a 'trustee' to deal with 'property' in a particular way, for the benefit of one or more 'beneficiaries'.</p>
                <h3 data-aos="fade-up">What is a 'trustee'?</h3>
                <p data-aos="fade-up">Trustees are the legal owners of the trust property. They are legally bound to look after the property of the trust in a particular way and for a particular purpose. Trustees administer the trust and in certain circumstances make decisions about how the property in trust is to be used.</p>
                <h3 data-aos="fade-up">What is property?</h3>
                <p data-aos="fade-up">The property of a trust can include:</p>
